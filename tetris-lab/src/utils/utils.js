export const DIRECTION = {
  up: "up",
  down: "down",
  left: "left",
  right: "right",
};

export const getOppositeDirection = function (direction) {
  if (direction === DIRECTION.down) {
    return DIRECTION.up;
  } else if (direction === DIRECTION.left) {
    return DIRECTION.right;
  } else {
    return DIRECTION.left;
  }
};
